/** Render `text` as a 5-row block-letter banner. Unknown chars become a filled box.
 *  Lowercase is upcased ‚ÄĒ the font is caps-only by design (reads as a wordmark). */
export function renderBanner(text: string, gap = 1): string {
  const glyphs = [...text.toUpperCase()].map((ch) => {
    const g = FONT[ch] ?? UNKNOWN;
    const w = Math.max(...g.map((r) => r.length));
    return g.map((r) => r.padEnd(w, " "));
  });
  const sep = " ".repeat(gap);
  const lines: string[] = [];
  for (let row = 0; row < ROWS; row++) {
    lines.push(glyphs.map((g) => g[row]).join(sep).replace(/\s+$/, ""));
  }
  return lines.join("\n");
}
